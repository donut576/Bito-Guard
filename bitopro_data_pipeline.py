swap.head()

# 訓練標籤與預測名單

train_label = fetch_table("train_label")
predict_label = fetch_table("predict_label")

print(train_label.shape)
print(predict_label.shape)

# 把資料存成 CSV
# 之後就不用重新抓 API

user_info.to_csv("user_info.csv", index=False)
twd_transfer.to_csv("twd_transfer.csv", index=False)
crypto_transfer.to_csv("crypto_transfer.csv", index=False)
trade.to_csv("trade.csv", index=False)
swap.to_csv("swap.csv", index=False)

train_label.to_csv("train_label.csv", index=False)
predict_label.to_csv("predict_label.csv", index=False)

# 將時間欄位轉為 datetime

twd_transfer["created_at"] = pd.to_datetime(twd_transfer["created_at"])
crypto_transfer["created_at"] = pd.to_datetime(crypto_transfer["created_at"])
trade["updated_at"] = pd.to_datetime(trade["updated_at"])
swap["created_at"] = pd.to_datetime(swap["created_at"])

# 有些金額需要乘 1e-8 才是實際值

twd_transfer["ori_samount"] = twd_transfer["ori_samount"] * 1e-8
crypto_transfer["ori_samount"] = crypto_transfer["ori_samount"] * 1e-8

# 將交易資料轉成 user-level 特徵

twd_feat = twd_transfer.groupby("user_id").agg({

    "ori_samount": ["count","sum","mean","max"]

})

twd_feat.columns = [

"twd_txn_count",
"twd_total_amount",
"twd_mean_amount",
"twd_max_amount"

]

twd_feat = twd_feat.reset_index()

twd_feat.head()

crypto_feat = crypto_transfer.groupby("user_id").agg({

    "ori_samount": ["count","sum","mean","max"]

})

crypto_feat.columns = [

"crypto_txn_count",
"crypto_total_amount",
"crypto_mean_amount",
"crypto_max_amount"

]

crypto_feat = crypto_feat.reset_index()

crypto_feat.head()

# 把所有特徵 merge 回 user_info

feature_df = user_info.copy()

feature_df = feature_df.merge(twd_feat, on="user_id", how="left")
feature_df = feature_df.merge(crypto_feat, on="user_id", how="left")

feature_df = feature_df.fillna(0)

feature_df.head()

# 建立訓練資料

train_df = feature_df.merge(train_label, on="user_id")

# 建立測試資料

test_df = feature_df.merge(predict_label, on="user_id")

print(train_df.shape)
print(test_df.shape)

