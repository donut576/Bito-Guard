# 虛擬幣轉帳資料

crypto_transfer = fetch_table("crypto_transfer")

print(crypto_transfer.shape)
crypto_transfer.head()

# USDT / TWD 交易

trade = fetch_table("usdt_twd_trading")

print(trade.shape)
trade.head()

# 一鍵買賣資料

swap = fetch_table("usdt_swap")

