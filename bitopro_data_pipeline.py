import pandas as pd
import numpy as np
import requests

from tqdm import tqdm

# 設定 API base url
# BitoPro 的資料是透過 PostgREST API 提供

BASE_URL = "https://aws-event-api.bitopro.com"

# 這個函式會自動從 API 抓取所有資料
# 因為 API 一次只能抓固定數量，所以需要 pagination

def fetch_table(table_name, limit=1000):

    offset = 0
    all_data = []

    while True:

        url = f"{BASE_URL}/{table_name}?limit={limit}&offset={offset}"

        response = requests.get(url)
        data = response.json()

        # 如果回傳資料為空代表抓完
        if len(data) == 0:
            break

        all_data.extend(data)

        offset += limit

        print(f"{table_name} fetched rows: {offset}")

    df = pd.DataFrame(all_data)

    return df

# user_info 是所有使用者的基本資料

user_info = fetch_table("user_info")

